#include <libft.h>

int		ft_isnumber(char *c)
{
	int		i;

	i = 0;
	while (c[i])
	{
		if ((c[i] < '0' || c[i] > '9') && c[i] != '+' && c[i] != '-')
			return (-1);
		i++;
	}
	return (0);
}
